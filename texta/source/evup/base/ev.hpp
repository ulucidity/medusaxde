class Ev
{
public:
    typedef Ev CDerives;
    ///////////////////////////////////////////////////////////////////////
    // Function: Order
    //
    //   Author: $author$
    //     Date: 6/21/2009
    ///////////////////////////////////////////////////////////////////////
    static UINT Order(UINT number) 
    {
        UINT order = 0;
        UINT ones = 0;

        for (; number > 1; number >>=1 )
        {
            ones += (number & 1);
            ++order;
        }
        return order + (0 < ones);
    }
};
